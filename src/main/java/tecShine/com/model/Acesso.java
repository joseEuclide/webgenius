package tecShine.com.model;

public class Acesso {

	
	
	private int id;
	private String usuarioAcesso;
	private String bi;
	private String senhaConfigurada;
	
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getUsuarioAcesso() {
		return usuarioAcesso;
	}
	public void setUsuarioAcesso(String usuarioAcesso) {
		this.usuarioAcesso = usuarioAcesso;
	}
	public String getBi() {
		return bi;
	}
	public void setBi(String bi) {
		this.bi = bi;
	}
	public String getSenhaConfigurada() {
		return senhaConfigurada;
	}
	public void setSenhaConfigurada(String senhaConfigurada) {
		this.senhaConfigurada = senhaConfigurada;
	}
	
	
	
}
